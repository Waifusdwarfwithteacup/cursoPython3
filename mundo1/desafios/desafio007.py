"""
Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre sua média.
"""
primeira_nota = float(input('Digite a 1ª nota do aluno: '))
segunda_nota = float(input('Digite a 2ª nota do aluno: '))
print(f'A média entre {primeira_nota:.1f} e {segunda_nota:.1f} é ígual a: {(primeira_nota + segunda_nota)/2:.1f}.')
